package com.abielinski.lsd.util;


public class Rectangle {
	public float x;
	public float y;
	public float width;
	public float height;
	public Rectangle(){
		x = 0;
		y = 0;
		width = 0;
		height = 0;
	}
	public Rectangle(float _x, float _y, float w, float h){
		x = _x;
		y = _y;
		width = w;
		height = h;
	}
}
